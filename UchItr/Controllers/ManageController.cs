        public async Task<ActionResult> MyPosts()
        {
            var currentUserID = User.Identity.GetUserId();
            var posts = from c in db.Posts.Include(p => p.Category).Include(p => p.User)
                         where c.UserID == currentUserID
                         select c;

            return View(await posts.ToListAsync());
        }

        public async Task<ActionResult> EditPost(int? id)
        {

            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            //Post post = await db.Posts.FindAsync(id);
            Post post = db.Posts.Include(p => p.Category).Include(p=>p.Image).Include(p => p.User).FirstOrDefault(t => t.Id == id);
            if (post == null)
            {
                return HttpNotFound();
            }
            ViewBag.CategoryID = new SelectList(db.Categorys, "Id", "Name", post.CategoryID);
            return View(post);
        }

        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> EditPost(EditPostViewModel postViewModel)
        {
            Post post = db.Posts.Include(p => p.Category).Include(p => p.User).Include(p => p.Image).FirstOrDefault(t => t.Id == postViewModel.Id);
            //Post post = db.Posts.Find(postViewModel.Id);
            if (ModelState.IsValid)
            {
                post.CategoryID = postViewModel.CategoryID;
                post.ShortDescription = postViewModel.ShortDescription;
                post.Body = postViewModel.Body;
                post.Published = postViewModel.Published;
                post.Title = postViewModel.Title;
                post.Image = postViewModel.Image;
                db.Entry(post).State = EntityState.Modified;
                await db.SaveChangesAsync();
                return RedirectToAction("MyPosts");
            }
            ViewBag.CategoryID = new SelectList(db.Categorys, "Id", "Name", post.CategoryID);
            return View(post);
        }

        // GET: Posts1/Delete/5
        public async Task<ActionResult> DeletePost(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Post post = await db.Posts.FindAsync(id);
            if (post == null)
            {
                return HttpNotFound();
            }
            return View(post);
        }

        // POST: Posts1/Delete/5
        [HttpPost, ActionName("DeletePost")]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> DeletePost(int id)
        {
            Post post = await db.Posts.FindAsync(id);
            db.Posts.Remove(post);
            await db.SaveChangesAsync();
            return RedirectToAction("MyPosts");
        }

